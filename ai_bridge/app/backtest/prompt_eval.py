# Built-in accept functions
# ══════════════════════════════════════════════════════════════════════
def baseline_accept_all(name: str = "baseline_accept_all") -> PromptVariant:
    """Accept every signal — reference benchmark."""
    return PromptVariant(name=name, accept=lambda _row: (True, {"variant": name}))


def simple_trend_filter(name: str = "ema_stack_filter") -> PromptVariant:
    """Accept only if EMA stack agrees with signal direction.

    Uses the engine's current stack; works for both smartgold (20/50/200)
    and psar_ema_vol (20/50/100/200) layouts.
    """
    def _accept(row: SignalRow) -> tuple[bool, dict]:
        is_long = "long" in row.signal or "bull" in row.signal
        # Prefer the bull_trend/bear_trend flags if present (psar_ema_vol)
        if row.bull_trend is not None or row.bear_trend is not None:
            ok = (is_long and bool(row.bull_trend)) or (
                not is_long and bool(row.bear_trend)
            )
        else:
            stacked_up = row.ema_fast > row.ema_slow > row.ema_base
            stacked_dn = row.ema_fast < row.ema_slow < row.ema_base
            ok = (is_long and stacked_up) or (not is_long and stacked_dn)
        return ok, {"variant": name}
    return PromptVariant(name=name, accept=_accept)


def strong_only_filter(name: str = "strong_only") -> PromptVariant:
    """Accept only ``strong_long`` / ``strong_short`` entries."""
    def _accept(row: SignalRow) -> tuple[bool, dict]:
        ok = row.signal in ("strong_long", "strong_short")
        return ok, {"variant": name, "signal": row.signal}
    return PromptVariant(name=name, accept=_accept)


def confidence_threshold_variant(
    min_confidence: float,
    system_prompt: str | None = None,
    name: str | None = None,
) -> PromptVariant:
    """LLM filter at a specific confidence threshold."""
    return PromptVariant(
        name=name or f"llm_min_conf_{min_confidence:.2f}",
        system_prompt=system_prompt,
        min_confidence=min_confidence,
    )


