"""Prompt-variant evaluator.

For each prompt variant (a ``system_prompt`` + minor behavioural tweaks),
the evaluator:

  1. Replays every historical signal generated by the chosen engine.
  2. For each ENTRY signal, asks the LLM (or a simple rule) whether to accept.
  3. Feeds the accepted signals to :func:`simulate_trades`.
  4. Aggregates the metrics with :func:`summarise`.

The engine can be either ``smartgold`` (legacy SMC) or ``psar_ema_vol``
(new PSAR+EMA+Volume strategy). For the latter, the simulator uses
``exit_mode="indicator"`` so engine-emitted exit_long / exit_short
signals are respected — closer to how the live system will behave.
"""
from __future__ import annotations

import asyncio
from collections.abc import Callable
from dataclasses import dataclass

import pandas as pd

from app.backtest.signals import SignalRow, get_engine
from app.backtest.simulator import simulate_trades, summarise
from app.config.settings import Settings, get_settings
from app.engine.decision_engine import evaluate_signal
from app.models.schemas import MacroContext, TradingViewAlert
from app.utils.logging import logger


@dataclass(slots=True)
class PromptVariant:
    """One named prompt configuration to evaluate."""

    name: str
    accept: Callable[[SignalRow], tuple[bool, dict]] | None = None
    system_prompt: str | None = None
    min_confidence: float | None = None

    def as_accept_fn(
        self, settings: Settings
    ) -> Callable[[SignalRow], tuple[bool, dict]]:
        if self.accept is not None:
            return self.accept

        min_conf = (
            self.min_confidence
            if self.min_confidence is not None
            else settings.min_confidence
        )

        def _accept(row: SignalRow) -> tuple[bool, dict]:
            alert = _row_to_alert(row)
            ctx = MacroContext(
                partial=True,
                notes=["backtest: no live macro context"],
            )
            decision = asyncio.run(evaluate_signal(alert, ctx, settings=settings))
            ok = (
                decision.action in {"execute", "reduce"}
                and decision.confidence >= min_conf
            )
            return ok, {
                "variant": self.name,
                "action": decision.action,
                "confidence": decision.confidence,
                "reasoning": decision.reasoning[:200],
            }

        return _accept


def _row_to_alert(row: SignalRow) -> TradingViewAlert:
    """Build a TradingViewAlert from a backtest SignalRow.

    Includes all modern PSAR+EMA+Volume fields when present, and keeps
    the legacy SMC fields populated when the row came from the
    ``smartgold`` engine.
    """
    return TradingViewAlert(
        secret="backtest",
        symbol=row.symbol,
        timeframe=row.timeframe,
        signal=row.signal,
        price=row.price,
        time=(
            row.ts.isoformat()
            if isinstance(row.ts, pd.Timestamp)
            else str(row.ts)
        ),
        # EMA ribbon — use best available mapping
        ema_fast=row.ema_fast or None,
        ema_mid=row.ema_mid,
        ema_slow=row.ema_slow or None,
        ema_base=row.ema_base or None,
        # PSAR + volume (new strategy)
        psar=row.psar,
        psar_below=row.psar_below,
        volume=row.volume,
        volume_sma=row.volume_sma,
        volume_ratio=row.volume_ratio,
        bull_trend=row.bull_trend,
        bear_trend=row.bear_trend,
        # Risk / position
        atr=row.atr or None,
        bars_since_entry=row.bars_since_entry,
        exit_reason=row.exit_reason,
        # Legacy SMC fields
        ms_state=row.ms_state,
        rsi=row.rsi,
        money_flow=row.money_flow,
    )


def run_backtest(
    df: pd.DataFrame,
    *,
    symbol: str,
    timeframe: str,
    variants: list[PromptVariant] | None = None,
    engine_name: str = "psar_ema_vol",
    stop_atr_mult: float = 1.5,
    rr: float = 2.0,
    max_bars: int = 48,
    exit_mode: str | None = None,
    engine_kwargs: dict | None = None,
    settings: Settings | None = None,
) -> dict:
    """Run the full backtest across all variants.

    Args:
        exit_mode: "fixed" or "indicator". If None, defaults to
                   "indicator" for ``psar_ema_vol`` and "fixed"
                   for everything else.
        engine_kwargs: extra kwargs forwarded to the engine (e.g. to
                       toggle ``emit_weak=True`` for the new engine).
    """
    s = settings or get_settings()
    engine_fn = get_engine(engine_name)
    signals = engine_fn(
        df, symbol=symbol, timeframe=timeframe, **(engine_kwargs or {})
    )
    resolved_exit_mode = exit_mode or (
        "indicator" if engine_name == "psar_ema_vol" else "fixed"
    )
    logger.info(
        "Backtest: {} bars, {} raw signals, engine={}, exit_mode={}",
        len(df), len(signals), engine_name, resolved_exit_mode,
    )

    if variants is None:
        variants = [baseline_accept_all()]

    out: dict = {
        "input": {
            "symbol": symbol,
            "timeframe": timeframe,
            "bars": int(len(df)),
            "signals": int(len(signals)),
            "engine": engine_name,
            "exit_mode": resolved_exit_mode,
            "stop_atr_mult": stop_atr_mult,
            "rr": rr,
            "max_bars": max_bars,
        },
        "variants": {},
    }

    for variant in variants:
        accept_fn = variant.as_accept_fn(s)

        accepted = 0
        rejected = 0

        def _counting_accept(
            row: SignalRow, _accept_fn=accept_fn
        ) -> tuple[bool, dict]:
            nonlocal accepted, rejected
            ok, meta = _accept_fn(row)
            if ok:
                accepted += 1
            else:
                rejected += 1
            return ok, meta

        trades = simulate_trades(
            df,
            signals,
            accept_fn=_counting_accept,
            stop_atr_mult=stop_atr_mult,
            rr=rr,
            max_bars=max_bars,
            exit_mode=resolved_exit_mode,
        )
        metrics = summarise(trades)
        out["variants"][variant.name] = {
            "accepted": accepted,
            "rejected": rejected,
            "metrics": metrics,
            "sample": [
                {
                    "entry_ts": str(t.entry_ts),
                    "exit_ts": str(t.exit_ts),
                    "side": t.side,
                    "outcome": t.outcome,
                    "reason": t.reason,
                    "pnl_r": t.pnl_r,
                    "bars_held": t.bars_held,
                }
                for t in trades[:5]
            ],
        }
        logger.info(
            "Variant '{}': accepted={} rejected={} win_rate={} expectancy_r={} pf={}",
            variant.name, accepted, rejected,
            metrics["win_rate"], metrics["expectancy_r"], metrics["profit_factor"],
        )

    return out


