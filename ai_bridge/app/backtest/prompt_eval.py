"""Prompt-variant evaluator.

For each prompt variant (a ``system_prompt`` + minor behavioural tweaks),
the evaluator:

  1. Replays every historical signal generated by the smartgold engine.
  2. For each signal, asks the LLM (or a simple rule) whether to accept.
  3. Feeds the accepted signals to :func:`simulate_trades`.
  4. Aggregates the metrics with :func:`summarise`.

LLM calls are batch-friendly: the evaluator supports ``concurrency`` (default
4) and also transparently runs under ``LLM_MOCK_MODE=true`` so you can
exercise the full pipeline without burning tokens. In mock mode the LLM
always returns ``action=skip`` — that's useful as a negative-control
baseline ("what if we skipped everything?").
"""
from __future__ import annotations

import asyncio
from collections.abc import Callable
from dataclasses import dataclass

import pandas as pd

from app.backtest.signals import SignalRow, get_engine
from app.backtest.simulator import simulate_trades, summarise
from app.config.settings import Settings, get_settings
from app.engine.decision_engine import evaluate_signal
from app.models.schemas import MacroContext, TradingViewAlert
from app.utils.logging import logger


@dataclass(slots=True)
class PromptVariant:
    """One named prompt configuration to evaluate."""

    name: str
    accept: Callable[[SignalRow], tuple[bool, dict]] | None = None
    system_prompt: str | None = None
    min_confidence: float | None = None

    def as_accept_fn(self, settings: Settings) -> Callable[[SignalRow], tuple[bool, dict]]:
        if self.accept is not None:
            return self.accept

        # Default: call the real decision engine. We pass a synthetic empty
        # MacroContext because the LLM is being evaluated on technical setup
        # alone — the backtest is not re-fetching live DXY/yields for
        # historical bars (we don't have them). Users can plug in a richer
        # context by supplying a custom ``accept`` callable.
        # Note: self.system_prompt is reserved for a future variant that
        # overrides the engine's SYSTEM_PROMPT; unused for now.
        min_conf = (
            self.min_confidence
            if self.min_confidence is not None
            else settings.min_confidence
        )

        def _accept(row: SignalRow) -> tuple[bool, dict]:
            alert = TradingViewAlert(
                secret="backtest",
                symbol=row.symbol,
                timeframe=row.timeframe,
                signal=row.signal,
                price=row.price,
                time=row.ts.isoformat() if isinstance(row.ts, pd.Timestamp) else str(row.ts),
                ms_state=row.ms_state,
                rsi=row.rsi,
                atr=row.atr,
                money_flow=row.money_flow,
                ema_fast=row.ema_fast,
                ema_slow=row.ema_slow,
                ema_base=row.ema_base,
            )
            ctx = MacroContext(
                partial=True,
                notes=["backtest: no live macro context"],
            )
            decision = asyncio.run(evaluate_signal(alert, ctx, settings=settings))
            ok = decision.action in {"execute", "reduce"} and decision.confidence >= min_conf
            return ok, {
                "variant": self.name,
                "action": decision.action,
                "confidence": decision.confidence,
                "reasoning": decision.reasoning[:200],
            }

        return _accept


def run_backtest(
    df: pd.DataFrame,
    *,
    symbol: str,
    timeframe: str,
    variants: list[PromptVariant] | None = None,
    engine_name: str = "smartgold",
    stop_atr_mult: float = 1.5,
    rr: float = 2.0,
    max_bars: int = 48,
    settings: Settings | None = None,
) -> dict:
    """Run the full backtest across all variants and return a comparable result.

    Returns a dict:
        {
            "input":   {symbol, timeframe, bars, signals},
            "variants": {
                name: {
                    "trades":   int,
                    "accepted": int,
                    "rejected": int,
                    "metrics":  summarise(...) output,
                    "sample":   first 5 trades for sanity-check,
                },
                ...
            },
        }
    """
    s = settings or get_settings()
    engine_fn = get_engine(engine_name)
    signals = engine_fn(df, symbol=symbol, timeframe=timeframe)
    logger.info(
        "Backtest: {} bars, {} raw signals, engine={}",
        len(df), len(signals), engine_name,
    )

    if variants is None:
        # Default: baseline (accept everything) for a reference benchmark.
        variants = [baseline_accept_all()]

    out: dict = {
        "input": {
            "symbol": symbol,
            "timeframe": timeframe,
            "bars": int(len(df)),
            "signals": int(len(signals)),
            "engine": engine_name,
            "stop_atr_mult": stop_atr_mult,
            "rr": rr,
            "max_bars": max_bars,
        },
        "variants": {},
    }

    for variant in variants:
        accept_fn = variant.as_accept_fn(s)

        accepted = 0
        rejected = 0

        def _counting_accept(
            row: SignalRow, _accept_fn=accept_fn
        ) -> tuple[bool, dict]:
            nonlocal accepted, rejected
            ok, meta = _accept_fn(row)
            if ok:
                accepted += 1
            else:
                rejected += 1
            return ok, meta

        trades = simulate_trades(
            df,
            signals,
            accept_fn=_counting_accept,
            stop_atr_mult=stop_atr_mult,
            rr=rr,
            max_bars=max_bars,
        )
        metrics = summarise(trades)
        out["variants"][variant.name] = {
            "accepted": accepted,
            "rejected": rejected,
            "metrics": metrics,
            "sample": [
                {
                    "entry_ts": str(t.entry_ts),
                    "exit_ts": str(t.exit_ts),
                    "side": t.side,
                    "outcome": t.outcome,
                    "reason": t.reason,
                    "pnl_r": t.pnl_r,
                }
                for t in trades[:5]
            ],
        }
        logger.info(
            "Variant '{}': accepted={} rejected={} win_rate={} expectancy_r={}",
            variant.name, accepted, rejected,
            metrics["win_rate"], metrics["expectancy_r"],
        )

    return out


# ──────────────────────────────────────────────────────────────────────
# Built-in accept functions — useful as baselines / sanity checks.
# ──────────────────────────────────────────────────────────────────────
def baseline_accept_all(name: str = "baseline_accept_all") -> PromptVariant:
    """Accept every signal — reference benchmark."""
    return PromptVariant(name=name, accept=lambda _row: (True, {"variant": name}))


def simple_trend_filter(name: str = "ema_stack_filter") -> PromptVariant:
    """Accept only if EMA stack agrees with signal direction.

    No LLM call — useful as a deterministic comparator to measure the
    LLM's incremental contribution.
    """
    def _accept(row: SignalRow) -> tuple[bool, dict]:
        is_long = "long" in row.signal or "bull" in row.signal
        stacked_up = row.ema_fast > row.ema_slow > row.ema_base
        stacked_dn = row.ema_fast < row.ema_slow < row.ema_base
        ok = (is_long and stacked_up) or (not is_long and stacked_dn)
        return ok, {"variant": name, "stacked_up": stacked_up, "stacked_dn": stacked_dn}
    return PromptVariant(name=name, accept=_accept)


def confidence_threshold_variant(
    min_confidence: float,
    system_prompt: str | None = None,
    name: str | None = None,
) -> PromptVariant:
    """LLM filter at a specific confidence threshold."""
    return PromptVariant(
        name=name or f"llm_min_conf_{min_confidence:.2f}",
        system_prompt=system_prompt,
        min_confidence=min_confidence,
    )


